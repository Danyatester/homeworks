$('.first').on('mousemove' , (e) =>{
	const x = e.pageX / $(window).width();
	const y = e.pageY / $(window).height();

	$('.body').css(
		'transform',
	    'translate(-' + x * 30 + 'px, -' + y * 30 + 'px)'
    );    


	$('.elfel').css(
		'transform',
	    'translate(-' + x * 60 + 'px, -' + y * 60 + 'px)'
    );        
});

